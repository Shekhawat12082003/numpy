# numpy provides function to perform log at base 2,e and 10 

# log at base 2 - we use log2() to perform log at the base 2 

import numpy as np
a = np.arange(1,10)
b = np.log2(a)
print(b)

# log at base 10 - we use log10() to perform log at the base 10 

import numpy as np
a = np.arange(1,10)
b = np.log10(a)
print(b)

